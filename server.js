//   initial();
// });

// simple route
app.get("/", (req, res) => {
  res.json({ message: "Welcome to Employee Management System (EMS)" });
});

// set port, listen for requests
const PORT = process.env.PORT || 3000;
app.listen(PORT, () => {
  console.log(`Server is running on port ${PORT}.`);
});

